									<p>
										My research focuses on coastal biogeochemistry and how natural and anthropogenic processes 
										influence benthic carbon and nutrient cycling. These topics are important because human 
										activities are rapidly altering these processes and the long-term effects of these changes are 
										largely unknown. Because a large portion of the human population lives adjacent to the coast, 
										we are tightly linked to the productivity and stability of coastal systems, and their ability to 
										mitigate anthropogenic changes.
									</p>
									<p>
										I use the eddy covariance technique to investigate the ambient, in-situ factors that control 
										carbon cycling such as: nutrients, irradiance, benthic communities, waves, and currents. My PhD 
										work is primarily on tropical coral reefs and seagrass meadows in south Florida. These results 
										reveal the complex interactions of hydrodynamics with productivity, the tight link to irradiance, 
										and complex benthic communities that efficiently harvest light to create linear photosynthesis to 
										irradiance curves. These results also show the importance of sampling in-situ over large spatial 
										scales as these effects are not able to be observed with any other methodology. Further, these 
										measurements show extremely high production rates on coral reefs and why these areas are hotspots 
										of biodiversity while being a large source of production for the surrounding areas.
									</p>
									<p> Current Location: Woods Hole Oceanographic Institution. </p>
